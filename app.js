/* ========================================
   Language Switcher
======================================== */
const langSwitch = document.querySelector('.lang-switch');
const langToggle = langSwitch.querySelector('.lang-toggle');
const langCurrent = langSwitch.querySelector('.lang-current');
const langMenu = langSwitch.querySelector('.lang-menu');
const langOptions = Array.from(langSwitch.querySelectorAll('.lang-option'));
const supportsIntersectionObserver = typeof window.IntersectionObserver === 'function';

function closeLangMenu() {
    langSwitch.classList.remove('open');
    langToggle.setAttribute('aria-expanded', 'false');
    langMenu.hidden = true;
}

function updateLangSwitcher(lang) {
    langCurrent.textContent = lang.toUpperCase();

    langOptions.forEach(btn => {
        const isCurrent = btn.dataset.lang === lang;
        btn.hidden = isCurrent;
        btn.disabled = isCurrent;
    });
}

function setLang(lang) {
    if (!dict[lang]) {
        lang = 'ar';
    }

    try {
        localStorage.setItem('lang', lang);
    } catch (error) {
        // Some mobile browsers can block storage in strict/private modes.
    }

    document.documentElement.lang = lang;
    
    // Handle RTL/LTR
    if (lang === 'ar') {
        document.body.classList.add('rtl');
        document.body.classList.remove('ltr');
    } else {
        document.body.classList.add('ltr');
        document.body.classList.remove('rtl');
    }
    
    // Update all translatable elements
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        if (dict[lang][key]) {
            // Allow HTML content for titles with spans
            if (key === 'title') {
                el.innerHTML = dict[lang][key];
            } else {
                el.textContent = dict[lang][key];
            }
        }
    });
    
    updateLangSwitcher(lang);
    closeLangMenu();
}

// Initialize language
let savedLang = 'ar';
try {
    savedLang = localStorage.getItem('lang') || 'ar';
} catch (error) {
    savedLang = 'ar';
}
setLang(savedLang);

langToggle.addEventListener('click', () => {
    const isOpen = langSwitch.classList.toggle('open');
    langToggle.setAttribute('aria-expanded', String(isOpen));
    langMenu.hidden = !isOpen;
});

langOptions.forEach(btn => {
    btn.addEventListener('click', () => {
        const lang = btn.dataset.lang;
        setLang(lang);
    });
});

document.addEventListener('click', (event) => {
    if (!langSwitch.contains(event.target)) {
        closeLangMenu();
    }
});

document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
        closeLangMenu();
    }
});

/* ========================================
   Carousel
======================================== */
class Carousel {
    constructor(container) {
        this.container = container;
        this.track = container.querySelector('.carousel-track');
        this.slides = container.querySelectorAll('.carousel-slide');
        this.prevBtn = container.querySelector('.carousel-prev');
        this.nextBtn = container.querySelector('.carousel-next');
        this.dotsContainer = container.querySelector('.carousel-dots');
        
        this.currentIndex = 0;
        this.totalSlides = this.slides.length;
        this.autoPlayInterval = null;
        const delayAttr = parseInt(container.dataset.autoplayDelay, 10);
        this.autoPlayDelay = Number.isFinite(delayAttr) && delayAttr > 0 ? delayAttr : 3000;
        this.manageVideos = container.dataset.carouselVideos === 'true';
        this.videoLoadingEnabled = !this.manageVideos;
        this.isDragging = false;
        this.startX = 0;
        this.currentX = 0;
        
        this.init();
    }
    
    init() {
        this.createDots();
        this.addEventListeners();
        if (this.manageVideos) {
            this.setupVideoLazyLoading();
        }
        this.updateCarousel();
        this.startAutoPlay();
    }

    setupVideoLazyLoading() {
        if (!supportsIntersectionObserver) {
            this.videoLoadingEnabled = true;
            return;
        }

        const videoObserver = new IntersectionObserver((entries) => {
            if (entries.some(entry => entry.isIntersecting)) {
                this.videoLoadingEnabled = true;
                this.updateCarousel();
                videoObserver.disconnect();
            }
        }, {
            rootMargin: '320px 0px',
            threshold: 0.01
        });

        videoObserver.observe(this.container);
    }

    loadVideoAt(index) {
        const slide = this.slides[index];
        if (!slide) return;

        const video = slide.querySelector('video[data-src]');
        if (!video || video.src) return;

        video.src = video.dataset.src;
        video.load();
    }

    loadNearbyVideos() {
        this.loadVideoAt(this.currentIndex);
        this.loadVideoAt((this.currentIndex + 1) % this.totalSlides);
    }
    
    createDots() {
        for (let i = 0; i < this.totalSlides; i++) {
            const dot = document.createElement('button');
            dot.classList.add('carousel-dot');
            if (i === 0) dot.classList.add('active');
            dot.addEventListener('click', () => this.goToSlide(i));
            this.dotsContainer.appendChild(dot);
        }
        this.dots = this.dotsContainer.querySelectorAll('.carousel-dot');
    }
    
    addEventListeners() {
        // Button clicks
        this.prevBtn.addEventListener('click', () => {
            this.prev();
            this.resetAutoPlay();
        });
        
        this.nextBtn.addEventListener('click', () => {
            this.next();
            this.resetAutoPlay();
        });
        
        // Touch/Mouse events for swipe
        this.track.addEventListener('mousedown', (e) => this.startDrag(e));
        this.track.addEventListener('touchstart', (e) => this.startDrag(e));
        
        document.addEventListener('mousemove', (e) => this.drag(e));
        document.addEventListener('touchmove', (e) => this.drag(e));
        
        document.addEventListener('mouseup', () => this.endDrag());
        document.addEventListener('touchend', () => this.endDrag());
        
        // Pause on hover
        this.container.addEventListener('mouseenter', () => this.stopAutoPlay());
        this.container.addEventListener('mouseleave', () => this.startAutoPlay());
    }
    
    startDrag(e) {
        if (e.target && e.target.closest && e.target.closest('video')) return;
        this.isDragging = true;
        this.startX = e.type === 'touchstart' ? e.touches[0].clientX : e.clientX;
        this.track.style.transition = 'none';
        this.stopAutoPlay();
    }
    
    drag(e) {
        if (!this.isDragging) return;
        
        const x = e.type === 'touchmove' ? e.touches[0].clientX : e.clientX;
        this.currentX = x - this.startX;
        const offset = -(this.currentIndex * 100) + (this.currentX / this.track.offsetWidth * 100);
        this.track.style.transform = `translateX(${offset}%)`;
    }
    
    endDrag() {
        if (!this.isDragging) return;
        this.isDragging = false;
        
        const threshold = 50;
        if (this.currentX > threshold) {
            this.prev();
        } else if (this.currentX < -threshold) {
            this.next();
        } else {
            this.updateCarousel();
        }
        
        this.track.style.transition = 'transform 0.5s cubic-bezier(0.4, 0, 0.2, 1)';
        this.currentX = 0;
        this.startAutoPlay();
    }
    
    updateCarousel() {
        this.track.style.transform = `translateX(-${this.currentIndex * 100}%)`;
        
        // Update dots
        this.dots.forEach((dot, index) => {
            dot.classList.toggle('active', index === this.currentIndex);
        });

        if (this.manageVideos) {
            if (this.videoLoadingEnabled) {
                this.loadNearbyVideos();
            }

            this.slides.forEach((slide, i) => {
                const video = slide.querySelector('video');
                if (!video) return;
                if (i !== this.currentIndex) {
                    video.pause();
                    video.currentTime = 0;
                }
            });
        }
    }
    
    goToSlide(index) {
        this.currentIndex = index;
        this.updateCarousel();
    }
    
    next() {
        this.currentIndex = (this.currentIndex + 1) % this.totalSlides;
        this.updateCarousel();
    }
    
    prev() {
        this.currentIndex = (this.currentIndex - 1 + this.totalSlides) % this.totalSlides;
        this.updateCarousel();
    }
    
    startAutoPlay() {
        if (this.manageVideos) return;

        this.stopAutoPlay();
        this.autoPlayInterval = setInterval(() => this.next(), this.autoPlayDelay);
    }
    
    stopAutoPlay() {
        if (this.autoPlayInterval) {
            clearInterval(this.autoPlayInterval);
            this.autoPlayInterval = null;
        }
    }
    
    resetAutoPlay() {
        this.startAutoPlay();
    }
}

